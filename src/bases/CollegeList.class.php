     * 通过学校ID或学校名获取该学校相关信息
     *
     * @param $univ_id
     * @param $univ_name
     * @return array(province_id,province_name,univ_id,univ_name)
     */
    function getUnivInfo($univ_id=NULL, $univ_name=NULL){
        if ($univ_id==NULL && $univ_name==NULL){
            return false;            
        }
        $list = self::getList();
        $count = count($list);
        $ret = array();

        //通过学校ID查找
        if ($univ_id != NULL){
            for($i=0; $i<$count; $i++){
                $univ_count = count($list[$i]['province_univs']);
                for($j=0; $j<$univ_count; $j++){
                    if ($univ_id == $list[$i]['province_univs'][$j]['univ_id']){
                        $ret['province_id'] = $list[$i]['province_id'];
                        $ret['province_name'] = $list[$i]['province_name'];
                        $ret['univ_id'] = $list[$i]['province_univs'][$j]['univ_id'];
                        $ret['univ_name'] = $list[$i]['province_univs'][$j]['univ_name'];
                        return $ret;
                    }
                }            
            }
        }

        //通过学校名查找
        if ($univ_name != NULL){
            for($i=0; $i<$count; $i++){
                $univ_count = count($list[$i]['province_univs']);
                for($j=0; $j<$univ_count; $j++){
                    if ($univ_name == $list[$i]['province_univs'][$j]['univ_name']){
                        $ret['province_id'] = $list[$i]['province_id'];
                        $ret['province_name'] = $list[$i]['province_name'];
                        $ret['univ_id'] = $list[$i]['province_univs'][$j]['univ_id'];
                        $ret['univ_name'] = $list[$i]['province_univs'][$j]['univ_name'];
                        return $ret;
                    }
                }            
            }
        }

        return false;
    }


    /**
     * 通过省份ID或省份名获取该省所有大学
     *
     * @param $province_id
     * @param $province_name
     * @return array(univ_id,univ_name), ...
     */
    function getUnivList($province_id=NULL, $province_name=NULL){
        if ($province_id==NULL && $province_name==NULL){
            return false;            
        }
        $list = self::getList();
        $count = count($list);

        //通过省份ID查找
        if ($province_id != ""){
            for($i=0; $i<$count; $i++){
                if ($province_id == $list[$i]['province_id']){
                    return $list[$i];
                }
            }
        }

        //通过省份名查找
        if ($province_name != ""){
            for($i=0; $i<$count; $i++){
                if ($province_name == $list[$i]['province_name']){
                    return $list[$i];
                }
            }
        }
        return false;
    }


    /**
     * 获取所有省份列表
     *
     */
